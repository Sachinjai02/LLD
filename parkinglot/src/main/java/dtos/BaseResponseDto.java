package dtos;

public class BaseResponseDto<T> {
    private String status;
    T data;

    public BaseResponseDto(T data) {
        this.data = data;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
